#Upper Case and Lower Case
#example
a="Hello World"
print(a.upper())
b="Hello Moon"
print(b.lower())

#Remove Whitespace
a1 = " Hello, World! "
print(a1.strip()) # returns "Hello, World!"

#Replace String
a2 = "Hello, World!"
print(a2.replace("H", "J"))

#Split String
a3 = "Hello, World!"
print(a3.split(",")) # returns ['Hello', ' World!']

